		    <p>My first experience with Gilman football came before I even entered Gilman. While I was in 5th grade, my mom met Joe Ehrmann and mentioned that I was about to enter Gilman and was interested in playing football. When she told me, I thought it was cool but didn't think much more about it. However, a couple weeks later, a letter arrived in the mail for me from Joe Ehrmann. He welcomed me to Gilman and said that he was excited to see me out on the football field. I eventually came to learn that this small gesture was a big part of Gilman football.
		    </p>

		    <p>Throughout my first 5 years at Gilman, I watched the varsity team win conference championship after conference championship and I couldn't wait to become a part of it. At the beginning of my junior year, I was on the team and I couldn't wait to start playing. Standing at 5' 9" 160lbs, I did not think I would get close to the field on game day, but practice would be fun. However, Coach Poggi did not overlook me and gave me a chance to play on the front line of kickoff return. I got smacked around, but just getting to go out on the field in front of the big crowds was an incredible experience.  Outside of the on field experiences that year, I really began to understand how Gilman had been successful for so long. Coach Poggi pushed us on the field to become better players and pushed us off the field to become better men, and the combination created an incredible culture that was hard to resist.
		    </p>
		    <p>Going into my senior year, I had high hopes for the team, but was not expecting a lot of playing time myself. I would have been happy leading the best scout defense Gilman has seen. Then Coach Gouline walked up to me and talked to me about playing offensive line. I was confused/excited at first, since I was still only about 170lbs  and everybody else was at least 200. However, I believed that Coach Poggi knew what he was doing and decided to give it a shot. It ended up working out well, except we nearly gave Coach White a heart attack every time he saw Ned Emala (the other midget lineman) and me next to each other in the starting line. The coaches found a spot for me on a team full of D1 talent.  Iâ€™m forever grateful that Coach Poggi put me into an unexpected position so that I could grow as a player.
		    </p>
		    <p>Being a part of the football team was one of the defining experiences of my time at Gilman. It was largely due to the emphasis that Coach Poggi placed on areas of life other than football. Going to Bible study every week, working at the Ronald McDonald house and becoming friends with many of my teammates will be what I remember far more than what happened on the field. 
		    </p>
		    <p>Thank you to Coach Poggi and all of the coaches that made this possible and helped me to become the man I am today.</p>

